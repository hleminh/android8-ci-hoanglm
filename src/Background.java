import java.awt.*;

public class Background {
    Image image;
    int x;
    int y;
    int width;
    int height;
    int speed;
    public Background(int a, int b, int c, int d, int f, Image g ){
        x = a;
        y = b;
        width = c;
        height = d;
        speed = f;
        image = g;
    }
}

